public class Divisao {

    /**
     * Realiza uma operação de divisão.
     *
     * @param n1 O primeiro operando.
     * @param n2 O segundo operando.
     * @return O resultado da operação.
     */
    public int divisao(int n1, int n2) {
            return n1 / n2;
    }
}