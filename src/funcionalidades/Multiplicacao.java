package funcionalidades;

/**
 *
 * @author Rafael Jakubovsky
 * @version 1.5
 * @since Release 1.0 da aplicação
 *
 * Classe simples que contém o método de multiplicação.
 */
public class Multiplicacao {

    /**
     * Realiza uma operação de multiplicação.
     *
     * @param n1 O primeiro operando.
     * @param n2 O segundo operando.
     * @return O resultado da operação.
     */
    public int multiplicacao(int n1, int n2) {
        return n1 * n2;
    }
}
