package funcionalidades;

/**
 *
 * @author Rafael Jakubovsky
 * @version 1.5.1
 * @since Release 1.0 da aplicação
 *
 * Classe simples que contém o método de subtração.
 */
public class Subtracao {

    /**
     * Realiza uma operação de subtração.
     *
     * @param n1 O primeiro operando.
     * @param n2 O segundo operando.
     * @return O resultado da operação.
     */
    public int subtracao(int n1, int n2) {
        return n1 - n2;
    }
}
